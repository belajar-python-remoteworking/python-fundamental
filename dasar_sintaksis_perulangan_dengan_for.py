Jumlah_buku=10
print('Ibu memberi perintah : "Baca semua bukumu"')

jumlah_buku_yg_sudah_dibaca = 0
print(f"Jumlah buku yg sudah dibaca: {jumlah_buku_yg_sudah_dibaca}")

